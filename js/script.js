// Package Modal Functions
function openPackageModal(packageId) {
    const packageInfo = packageData[packageId];
    const modal = document.getElementById('packageModal');

    // Update icon
    const icon = document.getElementById('packageIcon');
    icon.className = packageInfo.icon;

    // Update title and subtitle
    document.getElementById('packageTitle').textContent = packageInfo.title;
    document.getElementById('packageSubtitle').textContent = packageInfo.subtitle;

    // Update price
    const priceElement = document.getElementById('packagePrice');
    if (packageInfo.price) {
        priceElement.innerHTML = packageInfo.price;
        priceElement.style.display = 'block';
    } else {
        priceElement.style.display = 'none';
    }

    // Update price note
    const noteElement = document.getElementById('packageNote');
    if (packageInfo.priceNote) {
        noteElement.textContent = packageInfo.priceNote;
        noteElement.style.display = 'block';
    } else {
        noteElement.style.display = 'none';
    }

    // Update timings if available
    const timingsElement = document.getElementById('packageTimings');
    if (packageInfo.timings) {
        timingsElement.innerHTML = `<h4>${packageInfo.timings.title}</h4>` +
            packageInfo.timings.options.map(option => `<p><strong>${option}</strong></p>`).join('');
        timingsElement.style.display = 'block';
    } else {
        timingsElement.style.display = 'none';
    }

    // Update notes if available
    const notesElement = document.getElementById('packageNotes');
    if (packageInfo.notes) {
        notesElement.innerHTML = packageInfo.notes.map(note => `<p><strong>${note}</strong></p>`).join('');
        notesElement.style.display = 'block';
    } else {
        notesElement.style.display = 'none';
    }

    // Update features
    const featuresElement = document.getElementById('packageFeatures');
    featuresElement.innerHTML = packageInfo.features.map(feature =>
        `<li><i class="fas fa-check"></i> ${feature}</li>`
    ).join('');

    // Update additional info if available
    const additionalInfoContainer = document.getElementById('packageAdditionalInfo');
    if (packageInfo.additionalInfo) {
        additionalInfoContainer.innerHTML = `
            <h3 style="margin-top: 1.5rem; margin-bottom: 0.5rem; color: var(--secondary-color);">${packageInfo.additionalInfo.title}</h3>
            <div class="additional-info-content">
                ${packageInfo.additionalInfo.details.map(detail => detail ? `<p>${detail}</p>` : '<p>&nbsp;</p>').join('')}
            </div>
        `;
        additionalInfoContainer.style.display = 'block';
    } else {
        additionalInfoContainer.style.display = 'none';
    }

    modal.classList.add('show');
    document.body.style.overflow = 'hidden';
}

function closePackageModal() {
    const modal = document.getElementById('packageModal');
    modal.classList.remove('show');
    document.body.style.overflow = 'auto';
}

// Partner Modal Functions
function openPartnerModal(name, phone, email, iconClass, iconColorOrImage) {
    const modal = document.getElementById('partnerModal');
    document.getElementById('partnerName').textContent = name;
    document.getElementById('partnerPhone').textContent = phone;
    document.getElementById('partnerEmail').textContent = email;

    // Update the partner logo in the modal
    const modalHeader = document.querySelector('#partnerModal .modal-header');
    const existingIcon = modalHeader.querySelector('i');
    const existingImage = modalHeader.querySelector('img.partner-logo-img');

    if (iconClass === 'image') {
        // Remove icon if exists
        if (existingIcon) existingIcon.remove();

        // Add or update image
        if (existingImage) {
            existingImage.src = iconColorOrImage;
        } else {
            const img = document.createElement('img');
            img.src = iconColorOrImage;
            img.alt = name;
            img.className = 'partner-logo-img';
            img.style.width = '120px';
            img.style.height = '120px';
            img.style.objectFit = 'contain';
            img.style.marginBottom = '1rem';
            modalHeader.insertBefore(img, modalHeader.querySelector('h2'));
        }
    } else {
        // Remove image if exists
        if (existingImage) existingImage.remove();

        // Add or update icon
        if (existingIcon) {
            existingIcon.className = iconClass;
            existingIcon.style.color = iconColorOrImage;
        } else {
            const icon = document.createElement('i');
            icon.className = iconClass;
            icon.style.fontSize = '3rem';
            icon.style.color = iconColorOrImage;
            icon.style.marginBottom = '1rem';
            modalHeader.insertBefore(icon, modalHeader.querySelector('h2'));
        }
    }

    modal.classList.add('show');
    document.body.style.overflow = 'hidden'; // Prevent scrolling
}

function closePartnerModal() {
    const modal = document.getElementById('partnerModal');
    modal.classList.remove('show');
    document.body.style.overflow = 'auto'; // Re-enable scrolling
}

// Close modal when clicking outside of it
window.onclick = function(event) {
    const partnerModal = document.getElementById('partnerModal');
    const packageModal = document.getElementById('packageModal');

    if (event.target === partnerModal) {
        closePartnerModal();
    }
    if (event.target === packageModal) {
        closePackageModal();
    }
}

// Close modal with Escape key
document.addEventListener('keydown', function(event) {
    if (event.key === 'Escape') {
        closePartnerModal();
        closePackageModal();
    }
});

